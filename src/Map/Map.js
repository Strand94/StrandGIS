import React, { Component } from 'react';
import L from 'leaflet'
import {Map, TileLayer, Marker, Popup, GeoJSON} from 'react-leaflet';
import './Map.css';


var myIcon = L.icon({
  iconUrl: 'https://unpkg.com/leaflet@1.3.4/dist/images/marker-icon.png',
  iconSize: [26, 42],
  iconAnchor: [13, 42],
  popupAnchor: [0, -45],
});



class App extends Component {
  //Initial value for coordinated and zoom level
  state = {
    lat: 63.43,
    lng: 10.4,
    zoom: 13,
  }

  render() {
    //Set the map position and zoom level
    const position = [this.state.lat, this.state.lng]

    return (
      <Map className="map" center={position} zoom={this.state.zoom}>
        <TileLayer
          attribution="&amp;copy <a href=&quot;http://osm.org/copyright&quot;>OpenStreetMap</a> contributors"
          url="https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png"
        />
        <GeoJSON key="1" data={getGeoJsonTrondheim()} style={this.getStyle}>
          <Popup>
            this is Trondheim
          </Popup>
        </GeoJSON>
        <GeoJSON key="1" data={getGeoJsonTrondheim2()} style={this.getStyle}>
          <Popup>
            this is Trondheim2
          </Popup>
        </GeoJSON>
        <GeoJSON key="2" data={getGeoJsonLine()} style={this.getStyle}>
          <Popup>
            This is line
          </Popup>
        </GeoJSON>
        <Marker 
          position={position}
          icon = {myIcon}
        > 
      
          <Popup>
              This is a popup.
          </Popup>
        </Marker>
       
      </Map>
    );
  }
}

export default App;

